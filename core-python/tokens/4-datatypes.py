# A data type in Python is a classification that specifies which type of value a variable has and what type of operations can be performed on it. Python supports various built-in data types.

# Python's data types categorized by their general types, such as numeric, sequence, and so on:

# ▣ Numeric Types

# • Integer (int): Whole numbers.
# Examples: 10, -3, 0

# • Floating-point (float): Numbers with a decimal point.
# Examples: 3.14, -0.001, 2.718

# • Complex (complex): Numbers with a real and imaginary part.
# Examples: 1 + 2j, 3 - 4j

# ▣ Sequence Types

# • String (str): Ordered sequence of characters.
# Examples: "hello", 'world', "123"

# • List (list): Ordered, mutable collection of items.
# Examples: [1, 2, 3], ['apple', 'banana', 'cherry']

# • Tuple (tuple): Ordered, immutable collection of items.
# Examples: (1, 2, 3), ('apple', 'banana', 'cherry')

# ▣ Set Types

# • Set (set): Unordered collection of unique items.
# Examples: {1, 2, 3}, {'apple', 'banana', 'cherry'}

# • Frozen Set (frozenset): Immutable version of a set.
# Examples: frozenset([1, 2, 3]), frozenset({'apple', 'banana'})

# ▣ Mapping Type

# • Dictionary (dict): Unordered collection of key-value pairs.
# Examples: {'name': 'John', 'age': 30}, {'key1': 'value1', 'key2': 'value2'}

# ▣ None Type

# • None (NoneType): Represents the absence of a value or a null value.
# Example: None

# ▣ Boolean Type
# • Boolean (bool): Represents truth values, with two possible values: True and False.