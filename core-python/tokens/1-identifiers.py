# An identifier is a name given to entities like variables, functions, classes, modules, and other objects in Python. It is used to uniquely identify them.


# The rules for naming identifiers in Python are:

# • Identifiers can contain letters (uppercase and lowercase), digits, and underscores (_).
# • The name must start with a letter or an underscore.
# • Identifiers cannot start with a digit.
# • We cannot use special symbols like !, @, #, $, and so on.
# • It should not contain white space. first name
# • Identifiers are case-sensitive. firstname, Firstname, FIRSTNAME
# • Keywords cannot be used as identifiers.