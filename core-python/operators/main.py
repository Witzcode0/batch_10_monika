# Python operators are special symbols, combinations of symbols, or keywords that perform operations on values and variables (operands). Python classifies operators into the following main categories: 
    # Arithmetic Operators: Perform mathematical calculations. +, -. *, /, **, //, %
    # Assignment Operators: Assign values to variables. +=, -=, *=, /=, %=, **=, //=
    # Comparison (Relational) Operators: Compare values and return a boolean result (True or False). ==, !=, <, <=, >, >=
    # Logical Operators: Combine conditional statements using and, or, and not keywords. and, or, not
    # Identity Operators: Check if two variables point to the same object in memory using is and is not.
    # Membership Operators: Test if a value is present in a sequence (string, list, tuple, etc.) using in and not in.
    # Bitwise Operators: Perform operations on integers at the binary level.  &, |, ^, ~, <<, >>

